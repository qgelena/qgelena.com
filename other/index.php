			Приложение PowerPoint уже давно превратилось практически в синоним презентаций. У этой программы есть все необходимые инструменты для демонстрации профессионально сделанного слайд-шоу, снабженного аудио- и видеофрагментами, а также анимацией. Однако некоторые функции, с помощью которых можно создавать блестящие презентации, глубоко спрятаны в недрах интерфейса PowerPoint. Мы познакомим вас с целым рядом секретов PowerPoint.<br/>
