			Это легко  сделать  в среде PowerPoint, применив функцию  анимации.<br/>
			Выделите диаграмму и перейдите на вкладку «Анимация»,  находящуюся на  панели.  Выберите пункт «Область анимации».  В правой части окна приложения откроется дополнительная панель. Нажмите кнопку  «Добавить анимацию»  и в области «Вход»  выберите анимационный эффект,  например «Выцветание».<br/>
		</p>	
