			<strong>5. Настройка автоматической демонстрации</strong>
		</p>
		<p>	
			PowerPoint – отличный инструмент для создания слайд-шоу и его автоматической демонстрации на стенде компании во время торговой выставки. Для этого нужно задать автоматический режим воспроизведения, в котором слайды будут циклически сменять друг друга до получения команды на завершение презентации.<br/>
		</p>
		<p>	
			Чтобы перейти в режим автоматической презентации, щелкните на  вкладке  «Показ слайдов»  и нажмите кнопку  «Настройка демонстрации».  Установите переключатель  «Показ слайдов»  в положение «автоматический (полный экран)». Переключатель  «Смена слайдов»  установите в положение «по времени». Нажмите OK.<br/>
		</p>
		<p>	
			Выберите на  ленте  вкладку  «Переходы».  В разделе  «Смена слайда»  установите флажок  «После»  и укажите время (например, в случае установки значения 00:05,00 каждый слайд будет отображаться в течение 5  с).  Нажмите кнопку  «Применить  ко  всем»,  чтобы указанный переход затронул все слайды презентации. Теперь при воспроизведении презентации слайды будут автоматически сменять друг друга через каждые  5 с.  По достижении последнего слайда цикл демонстрации повторится. Чтобы завершить показ, нажмите  <Esc>.<br/>
		</p>	
			<img src="../static/img5.jpg" alt="presentation" width="550" height="450" /><br/>
		<p>	
			<em>Во время торговой выставки можно настроить презентацию таким образом,  чтобы ее воспроизведение осуществлялось автоматически.  После демонстрации последнего слайда цикл воспроизведения будет  повторяться</em><br/>
		</p>	
		<p>	
			<strong>6. Повторное использование слайдов другой презентации</strong><br/>
		</p>
		<p>	
			При демонстрации слайд-шоу вам, возможно, понадобится слайд, уже  имеющийся  в одной из ранее созданных  презентаций, например  слайд, представляющий ключевых сотрудников вашей компании или информацию о  контактах. Чтобы повторно использовать такой контент,  импортируйте существующие слайды в текущую презентацию.<br/>
		</p>
		<p>	
			Перейдите на основной панели на вкладку «Главная» и в меню «Создать слайд» выберите пункт «Повторное использование слайдов». На панели «Повторное  использование  слайдов» нажмите кнопку «Обзор», выберите пункт «Поиск файла» и укажите презентацию PowerPoint, содержащую те  слайды, которые вы хотели бы применять. Список слайдов отобразится на дополнительной панели в правой части экрана. Чтобы увеличить слайд, подведите  к  нему указатель мыши.  Щелкните  на слайде,  который должен быть добавлен в новую презентацию. PowerPoint изменит его формат, чтобы он соответствовал стилю презентации. Если вы предпочитаете использовать форматирование оригинальной презентации, перед выполнением процедуры импорта установите флажок «Сохранить  исходное  форматирование», расположенный в нижней части панели «Повторное использование слайдов».
		</p>	
			<img src="../static/img6.jpg" alt="presentation" width="550" height="450" /><br/>
		<p>
			<em>Функция повторного использования слайдов позволяет импортировать слайды из других презентаций, сохраняя их форматирование
			в неизменном виде или приводя его к стилю новой  презентации</em> 
		</p>	
