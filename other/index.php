			<strong>3. Cоздание стилей презентации с помощью  «Образца слайдов»</strong><br/>
		</p>
		<p>	 
			Наверное, вы уже знакомы с использованием в Microsoft Word и Publisher стилей, предназначенных для единообразного форматирования текста в документе. Хотя PowerPoint и не поддерживает стилей, аналогичные операции  поможет  выполнить  функция «Образец слайдов». Если вы внесете в нее изменения, то измените  форматирование текста на всех слайдах одновременно.
			Чтобы перейти в этот режим, перейдите на  панели  на вкладку  «Просмотр»  и нажмите кнопку  «Образец слайдов». Слева на экране  откроется меню, в верхней части которого будет представлен  «Образец слайдов»,  а ниже  будут размещены  доступные схемы их компоновки.<br/>
		</p>
		<p>	
			Чтобы  привести  текст  к требующемуся виду,  выберите на  «Образце слайдов»  соответствующий элемент. К примеру,  если нужно  изменить форматирование заголовков, выделите текст заголовка и настройте его  так, как вам хочется.  Здесь  допустимо  изменять как отдельные стили текста, так и сразу все стили.<br/>
		</p>
		<p>	
			Потом  перейдите на вкладку  «Образцы слайдов»  и нажмите кнопку  «Закрыть  режим  образца»,  чтобы вернуться к редактированию презентации. Внесенные изменения автоматически отобразятся на всех слайдах презентации, а форматирование  «Образца слайдов»  будет применяться ко всем новым слайдам.<br/>
		</p>
