			<strong>2. Отображение картинки в нужный момент</strong><br/>
		</p>
		<p>	
			Нередко у авторов презентаций возникает желание спрятать картинку, находящуюся на слайде, с  тем  чтобы открыть ее в нужный момент.  Чтобы проделать это, нужно привязать  изображение к  фигуре. А затем следует настроить  параметры таким образом, чтобы после  того,  как вы щелкните  на  фигуре мышью, картинка отобразилась на экране.  Добавьте на слайд изображение и вставьте  ту  фигуру, щелчок  на  которой приведет к отображению картинки. На вкладке «Вставка»  нажмите кнопку  «Фигуры».  Выберите какую-нибудь фигуру, например  «Овал»,  и нарисуйте ее на слайде. Щелкните  на  фигуре правой кнопкой мыши, выберите из контекстного меню пункт «Изменить текст»  и введите какую-нибудь фразу,  например «Щелкни на мне!»<br/>
		</p>
		<p>	
			Выделите изображение, перейдите на вкладку  «Анимация»  и нажмите кнопку  «Добавить анимацию».  Укажите вид анимации, например «Масштабирование».  Выведите на экран панель  «Область анимации»,  нажав соответствующую этому названию кнопку.  Щелкните правой кнопкой мыши  на элементе, находящемся  в  «Области анимации»,  и выберите из контекстного меню пункт «Время».  Нажмите кнопку  «Переключатели»  и установите переключатель в положение  «Начать  выполнение эффекта при  щелчке».  В  ниспадающем  списке выберите название созданной вами фигуры. Нажмите OK.<br/>
		</p>
		<p>	
			Для просмотра полученного эффекта перейдите на  ленте  на вкладку «Показ слайдов»  и нажмите кнопку «С текущего слайда». В момент вывода слайда на экран картинка на нем не  отображается, она  появляется лишь после того, как вы щелкните мышью  на  фигуре.<br/>
