			<strong>1. Поэтапное отображение диаграммы</strong><br/>
		</p>
		<p>	
			Диаграмма на слайде гораздо эффектнее будет  выглядеть  в пошаговом режиме, когда ее ряды выводятся на экран не одновременно, а последовательно.<br/>
