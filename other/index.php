		<p>
			<em>PowerPoint автоматически разделяет процесс отображения диаграммы на несколько этапов. Отдельные области диаграммы появляются на экране не одновременно, а в определенной последовательности</em><br/> 
 		</p>
 		<p>	
 			На панели «Область анимации»  щелкните правой кнопкой мыши  на элементе, появившемся  там после добавления эффекта. Выберите из контекстного меню пункт  «Параметры эффектов»  и перейдите на вкладку  «Анимация диаграммы».  По умолчанию параметр «Группировка диаграммы»  принимает значение  «Как  один  объект».  В этом случае все элементы диаграммы отображаются на экране одновременно. Чтобы  последовательно  выводить ряды,  выберите из  ниспадающего  меню пункт  «По рядам».  Ряды будут отображаться один за другим с некоторой задержкой. При выборе пункта  «По категориям»  на каждом этапе на экран будет выводиться очередная категория. Установите флажок  «Запустить анимацию»,  нарисовав фон диаграммы, и перейдите на вкладку  «Время».  Для автоматического запуска анимации при отображении на экране слайда для параметра  «Начало» установите  значение 
 			«После предыдущего».<br/> 
 		</p>
